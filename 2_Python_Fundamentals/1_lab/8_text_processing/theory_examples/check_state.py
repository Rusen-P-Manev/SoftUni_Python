word = '         HASDFPOIU7890'


print("1", word.isalnum()) #check if all char are alphanumeric
print("2", word.isalpha()) #check if all char in the string are alphabetic
print("3", word.isdigit()) #test if string contains digits
print("4", word.istitle()) #test if string contains title words
print("5", word.isupper()) #test if string contains upper case
print("6", word.islower()) #test if string contains lower case
print("7", word.isspace()) #test if string contains spaces
print("8", word.endswith('d')) #test if string endswith a d
print("9", word.startswith('H')) #test if string startswith H
print("10", word.strip()) #test if string startswith H
print("11", word.lstrip()) #test if string startswith H
print("12", word.rstrip()) #test if string startswith H